#include "../../inc/libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	len;

	i = ft_strlen(dst);
	j = ft_strlen(src);
	if (size <= i)
	{
		len = j + size;
		return (len);
	}
	else
		len = i + j;
	j = 0;
	while (j + i < size - 1 && src[j] != '\0')
	{
		dst[i + j] = src[j];
		j++;
	}
	dst[i + j] = '\0';
	return (len);
}
